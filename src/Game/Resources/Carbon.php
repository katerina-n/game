<?php

namespace BinaryStudioAcademy\Game\Resources;

class Carbon
{
    public $carbon = 'carbon';

    public function setCarbon($carbon)
    {
        $this->carbon = $carbon;
    }

    /**
     * Gets Carbon.
     *
     * @return mixed
     */
    public function getCarbon()
    {
        return $this->carbon;
    }

}